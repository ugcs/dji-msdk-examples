package com.example.ugcssample.model.dto;

public class Vector2d {
    /**
     * the x-component of this vector
     **/
    public double x;
    /**
     * the y-component of this vector
     **/
    public double y;

    public Vector2d(double x, double y) {
        this.x = x;
        this.y = y;
    }
}
