package com.example.ugcssample.model.dto;

public class Vector3d {
    /**
     * the x-component of this vector
     **/
    public double x;
    /**
     * the y-component of this vector
     **/
    public double y;
    /**
     * the z-component of this vector
     **/
    public double z;

    public Vector3d(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
